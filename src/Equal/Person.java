package Equal;

public class Person {
    int a;
    int b;
    String s1;
    String s2;

    public Person(int a, int b, String s1, String s2) {
        this.a = a;
        this.b = b;
        this.s1 = s1;
        this.s2 = s2;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public String getS1() {
        return s1;
    }

    public void setS1(String s1) {
        this.s1 = s1;
    }

    public String getS2() {
        return s2;
    }

    public void setS2(String s2) {
        this.s2 = s2;
    }
}
